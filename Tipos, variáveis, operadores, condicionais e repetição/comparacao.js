// Igual (==)
// Retorna verdadeiro caso os operadores sejam iguais. -3 == var1
"3" == var1

//NÃ£o igual (1-)
//Retorna verdadeiro caso os operandos nao sejam iguais.-var1 != 4
var2 != "3"

//Estritamente igual (===)
//Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo. Veja tambem object.js e igualdade
3 === var1

//Estritamente nao igual (!==)
// Retorna verdadeiro caso os operando nao sejam iguais e/ou nao sejam do mesmo tipo.
var1 !== "3"
3 !== '3'

//Maior que (>)
//Retorna verdadeiro caso o operando da esquerda seja maior que o da direita
var2 > var1
"12" > 2

//Maior que ou igual (>=)
//Retorna verdadeiro caso o operando da esquerda seja maior ou igual ao da direita.
var2 >= var1
var1 >= 3

//Menor que (<)
//Retorna verdadeiro caso o operando da esquerda seja menor  que da direita.
var1 < var2
"12" < "2"

//Menor que ou igual (<=)
//Retorna verdadeiro caso o operando da esquerda seja menor ou igual ao da direita.
var1 <= var2
var2 <= 5